#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const starterRoot = path.join(repoRoot, 'examples/custom-frontend-next');
const outputPath = path.join(repoRoot, 'apps/public/src/lib/customFrontendStarterProjectTemplate.ts');

const starterFiles = [
  { path: 'README.md', role: 'starter-documentation' },
  { path: 'package.json', role: 'runtime-package' },
  { path: 'next.config.mjs', role: 'next-config' },
  { path: 'tsconfig.json', role: 'typescript-config' },
  { path: 'src/app/layout.tsx', role: 'app-layout' },
  { path: 'src/app/styles.css', role: 'app-styles' },
  { path: 'src/app/[[...path]]/page.tsx', role: 'public-page-renderer' },
  { path: 'src/app/api/backy-connection/route.ts', role: 'connection-probe' },
  { path: 'src/app/api/newsletter/route.ts', role: 'newsletter-bridge' },
  { path: 'src/app/api/backy-form/route.ts', role: 'form-bridge' },
  { path: 'src/lib/backy-client.ts', role: 'backy-public-client' },
  { path: 'src/lib/backy.ts', role: 'backy-client-bootstrap' },
  { path: 'src/lib/render.tsx', role: 'backy-renderer' },
];

const readStarterFile = (relativePath) => {
  const absolutePath = path.join(starterRoot, relativePath);
  if (!absolutePath.startsWith(starterRoot + path.sep)) {
    throw new Error(`Refusing to read outside starter root: ${relativePath}`);
  }
  return fs.readFileSync(absolutePath, 'utf8');
};

const entries = starterFiles.map((file) => ({
  path: file.path,
  role: file.role,
  source: 'examples/custom-frontend-next',
  content: readStarterFile(file.path),
}));

const output = `// Generated by scripts/generate-custom-frontend-starter-template.mjs.
// Do not edit manually; update examples/custom-frontend-next and regenerate.

export interface CustomFrontendStarterProjectTemplateFile {
  path: string;
  role: string;
  source: "examples/custom-frontend-next";
  content: string;
}

export const CUSTOM_FRONTEND_STARTER_TEMPLATE_ROOT = "examples/custom-frontend-next";

export const CUSTOM_FRONTEND_STARTER_TEMPLATE_FILES = ${JSON.stringify(entries, null, 2)} satisfies CustomFrontendStarterProjectTemplateFile[];
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, output);
console.log(`Generated ${path.relative(repoRoot, outputPath)} with ${entries.length} starter files.`);
